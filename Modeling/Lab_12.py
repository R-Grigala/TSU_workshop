import random

# =====================================================
# ამოცანა 12
# ბერნულის განაწილების გენერაცია
# X ~ Be(p)
# =====================================================

def bernoulli(p):
    """
    ბერნულის შემთხვევითი სიდიდის გენერაცია

    p -- წარმატების ალბათობა (0 <= p <= 1)

    აბრუნებს:
    1 ალბათობით p
    0 ალბათობით 1 - p
    """

    # ერთგვაროვანი შემთხვევითი რიცხვი (0,1)
    U = random.random()

    # ბერნულის წესი
    if U < p:
        return 1
    else:
        return 0


# =====================================================
# ტესტირება / მაგალითები
# =====================================================

if __name__ == "__main__":
    """
    (მაგალითად, p=0.5-ის შემთხვევაში უნდა იყოს დაახლოებით თანაბარი რაოდენობის 0-ები და 1-ები)
    """
    print("ამოცანა 12 — ბერნულის განაწილება (p = 0.5):")
    bernoulli_samples = [bernoulli(0.5) for _ in range(20)]
    print(bernoulli_samples)
