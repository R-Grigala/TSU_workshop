def generate_bits():
    """
    ეს ფუნქცია გენერირებს ბიტების მიმდევრობას {b_i}, i=1,...,64,
    ტაუსვორთის ტიპის გენერატორის გამოყენებით ორი კუდგივიქნებით.
    რეკურენტული წესი: b_i = (b_{i-2} + b_{i-11}) % 2, i > 11.
    საწყისი მნიშვნელობები: b_i = 1, თუ i <= 11.
    შენიშვნა: დოკუმენტში მოცემული ფორმულა (i-1 და i-4) სავარაუდოდ ტიპოგრაფიული შეცდომაა,
    რადგან მოცემულია r=2, q=11, ამიტომ გამოყენებულია i-r და i-q.
    """
    b = [0] * 65  # მასივი ინდექსებით 1-დან 64-მდე
    for i in range(1, 12):
        b[i] = 1  # საწყისი ბიტები 1-დან 11-მდე = 1
    for i in range(12, 65):
        b[i] = (b[i-2] + b[i-11]) % 2  # რეკურენტული გამოთვლა (XOR ექვივალენტური mod 2-ში)
    return [b[i] for i in range(1, 65)]  # დაბრუნება მიმდევრობის სიის სახით

# მიმდევრობის გენერაცია
bits = generate_bits()
sequence = ''.join(map(str, bits))  # ორობითი სტრიქონი
decimal = int(sequence, 2)  # ათობითში გადაყვანა

# გამოტანა (ტესტირებისთვის, არ არის ფუნქციის ნაწილი)
print("ორობითი მიმდევრობა:", sequence)
print("ათობითი ექვივალენტი:", decimal)