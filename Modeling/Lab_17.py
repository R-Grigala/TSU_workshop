def generate_bits():
    """
    ეს ფუნქცია გენერირებს ბიტების მიმდევრობას {b_i}, i=1,...,32,
    ტაუსვორთის ტიპის გენერატორის გამოყენებით ორი კუდგივიქნებით.
    რეკურენტული წესი: b_i = (b_{i-2} + b_{i-5}) % 2, i > 5.
    საწყისი მნიშვნელობები: b_i = 1, თუ i <= 5.
    შენიშვნა: დოკუმენტში მოცემული ფორმულა (i-1 და i-4) სავარაუდოდ ტიპოგრაფიული შეცდომაა,
    რადგან მოცემულია r=2, q=5, ამიტომ გამოყენებულია i-r და i-q.
    """
    b = [0] * 33  # მასივი ინდექსებით 1-დან 32-მდე
    for i in range(1, 6):
        b[i] = 1  # საწყისი ბიტები 1-დან 5-მდე = 1
    for i in range(6, 33):
        b[i] = (b[i-2] + b[i-5]) % 2  # რეკურენტული გამოთვლა (XOR ექვივალენტური mod 2-ში)
    return [b[i] for i in range(1, 33)]  # დაბრუნება მიმდევრობის სიის სახით

# მიმდევრობის გენერაცია
bits = generate_bits()
sequence = ''.join(map(str, bits))  # ორობითი სტრიქონი
decimal = int(sequence, 2)  # ათობითში გადაყვანა

# გამოტანა (ტესტირებისთვის, არ არის ფუნქციის ნაწილი)
print("ორობითი მიმდევრობა:", sequence)
print("ათობითი ექვივალენტი:", decimal)