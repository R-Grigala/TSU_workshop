import random

def generate_X():
    """
    ფუნქცია აგენერირებს უწყვეტ შემთხვევით სიდიდეს X
    ერთგვაროვანი განაწილებით ინტერვალზე [23.4, 53.02]
    შებრუნებული გარდაქმნის მეთოდის გამოყენებით
    """

    # 1. შემთხვევითი რიცხვის გენერაცია U ~ Uniform(0,1)
    U = random.random()

    # 2. განაწილების პარამეტრები
    a = 23.4
    b = 53.02

    # 3. შებრუნებული გარდაქმნის ფორმულა
    X = a + (b - a) * U

    # 4. დაბრუნება
    return X


# ფუნქციის ტესტირება
for i in range(5):
    print(f"გენერირებული X_{i+1} = {generate_X():.4f}")
